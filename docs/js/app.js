var frames = {curImg: 0};
var headerBackground = "#header-background";
var headerContentTop = "#header-content-top";
var headerContentBottom = "#header-content-bottom";

gsap.registerPlugin(ScrollTrigger);

gsap.to(frames, {
    scrollTrigger: {
        start: "400px top",
        end: "+=4000px bottom",
        //pin: true,
        scrub: true,
        snap: 1 / (images.length - 1),
        markers: true
    },
    curImg: images.length - 1,
    roundProps: "curImg",
    immediateRender: true,
    ease: "none",
    onUpdate: function () {
        $(headerBackground).attr("src", images[frames.curImg]);
    }
});

gsap.to(headerContentTop, {
    scrollTrigger: {
        start: "100px top",
        end: "+=300px top",
        //pin: true,
        scrub: true,
        //markers: true
    },
    opacity: 0,
    ease: "none",
    duration: 3
});

gsap.to(headerContentBottom, {
    scrollTrigger: {
        start: "3400px top",
        end: "+=300px top",
        //pin: true,
        scrub: true,
        //markers: true
    },
    opacity: 1,
    ease: "none",
    duration: 3
});